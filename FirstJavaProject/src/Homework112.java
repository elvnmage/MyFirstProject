/* Task 1.1 
 * Write a program which will print text “It is not a bug, 
 * it is a feature” 5 times, each line should be printed from new string 
 */

public class Homework112 // Version 2

{
	public static void main (String args[])
	
	{
		int x = 5;
		
		while (x > 0)
		{
			System.out.println("It is not a bug, it is a feature.");
	    
			x--;		
	    
		}
		
		
	}
		
	
}
